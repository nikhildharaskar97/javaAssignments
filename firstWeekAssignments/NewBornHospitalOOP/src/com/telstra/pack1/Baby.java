package com.telstra.pack1;

public class Baby {
	private String name;
	private double weight;
	public Baby(String name, double weight) {
		super();
		this.name = name;
		this.weight = weight;
	}
	
	public String getName() {
		return this.name;
	}
	
	public double getWt() {
		return this.weight;
	}
}
